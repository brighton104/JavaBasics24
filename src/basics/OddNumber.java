package basics;

public class OddNumber {

	public static void main(String[] args) {
		 //odd number
	    int c=1;
	    while (c<=10)
	    {
			
			System.out.println(c);
			c=c+2;
		}
	    
	    
	    int d=1;
	    do
	    {
		System.out.println(d);
		d=d+2;
	    }
	    while (d<=10);
	    	
	}

}
