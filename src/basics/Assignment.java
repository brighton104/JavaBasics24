package basics;

public class Assignment {

	public static void main(String[] args) {

		int a=55;
		System.out.println(a);
		
		int b=a;
		System.out.println(b);
		
		b+=a;
	    //b=b+a
		//b=55+55
		System.out.println(b);
		
		
		b-=a;
		//b=b-a
		//b=110-55
		System.out.println(b);
		
		b*=a;
		//b=b*a;
		//b=55*55
		System.out.println(b);
		
		b/=a;
		//b=b/a
		//b=3025/55
		System.out.println(b);
		
		b%=a;
		//b=b%a
		//b=55%55
		System.out.println(b);
		
		
		
	}

}
