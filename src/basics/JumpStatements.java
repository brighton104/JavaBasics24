package basics;

public class JumpStatements {

	public static void main(String[] args) {
	
	System.out.println("Break");
	for(int i=1;i<=10;i++)
	{
	
		if (i==5)
		{	
			break;
		}

		System.out.println(i);
}
	
	
System.out.println("Continue");
	
	for(int j=1;j<=10;j++)
	{
	
		if (j==5)
		{	
			continue;
		}

		System.out.println(j);
}
	
	
	
	
	
	
	
	
}
}
